class Map{
    constructor(player, elSize){
        this.player = player;
        this.elSize = elSize;
        this.level = 0;
        this.mapCols;
        this.emojis = {
            'X':'ðŸŒ³',
            'O': 'ðŸª¨',
            '-': " ",
            'I':'ðŸ¤ '        }
    }

    mapBuild(){
        const map = maps[this.level];
        const mapRows = map.trim().split("\n");
        this.mapCols = mapRows.map(row => row.trim().split(''));
    }

    render(game){
        this.mapCols.forEach((row, y) => {
            row.forEach((col, x) => {
                if (col == 'I') {
                    this.player.y = y;
                    this.player.x = x;
                }
                game.fillText(this.emojis[col],this.elSize * (x+1), this.elSize * (y+1));
            });
        })
    }
}










export default Map;

